#include <iostream>    // Для ввода/вывода (cin, cout)
#include <iomanip>     // Для форматирования вывода (setw)

using Matrix = int[10][10];  // Создаем псевдоним типа для матрицы 10x10
using namespace std;   // Использование стандартного пространства имен
int main() {
    // Инициализация матрицы 10x10 нулями
    Matrix A = {0};
    // Объявление переменных для индексов
    size_t i = 0;  // Индекс строки
    size_t j = 0;  // Индекс столбца

    // Ввод матрицы от пользователя
    while (i < 10) {
        j = 0;  // Сбрасываем индекс столбца для каждой новой строки
        while (j < 10) {
            cout << i << ' ' << j << ':';// Выводим текущие индексы 
            cin >> A[i][j];  // Читаем элемент матрицы
            j++;             // Переходим к следующему столбцу
            cout << endl;   // Новая строка
        }
        i++;  // Переходим к следующей строке
    }

    // Вывод исходной матрицы
    cout << "User array:" << "\n";

    i = 0;  // Сбрасываем индекс строки
    while (i < 10) {
        j = 0;  // Сбрасываем индекс столбца
        while (j < 10) {
            cout << std::setw(4) << A[i][j];  // Выводим элемент с шириной 4 символа
            j++;  // Переходим к следующему столбцу
        }
        cout << '\n';  // Переход на новую строку после каждой строки матрицы
        i++;  // Переходим к следующей строке
    }
    cout << '\n';  // Дополнительный перевод строки

    // Поиск максимального элемента в матрице
    int maxVal = 0;           // Переменная для хранения максимального значения
    size_t maxRow = 0;   // Строка с максимальным элементом
    size_t maxCol = 0;   // Столбец с максимальным элементом

    i = 0;  // Сбрасываем индекс строки
    while (i < 10) {
        j = 0;  // Сбрасываем индекс столбца
        while (j < 10) {
            if (A[i][j] > maxVal) {
                maxVal = A[i][j];  // Обновляем максимальное значение
                maxRow = i;        // Запоминаем строку
                maxCol = j;        // Запоминаем столбец
            }
            j++;  // Переходим к следующему столбцу
        }
        i++;  // Переходим к следующей строке
    }

    // Вывод информации о максимальном элементе
    // Прибавляем 1 к индексам, так как пользователь ожидает нумерацию с 1, а не с 0
    cout << "Max element: " << maxVal
              << " (row " << maxRow + 1
              << ", column " << maxCol + 1 << ")\n\n";

    // Замена элементов на четных позициях на максимальное значение
    i = 0;
    while (i < 10) {
        j = 0;
        while (j < 10) {
            // Проверяем, находятся ли индексы на четных позициях
            // (i+1)%2 == 0 - четная строка (2,4,6,8,10)
            // (j+1)%2 == 0 - четный столбец (2,4,6,8,10)
            if (((i + 1) % 2 == 0) && ((j + 1) % 2 == 0)) {
                A[i][j] = maxVal;  // Заменяем элемент на максимальное значение
            }
            j++;
        }
        i++;
    }

    // Вывод результирующей матрицы
    cout << "Result array:" << "\n";
    i = 0, j = 0;  // Сбрасываем индексы
    while (i < 10) {
        j = 0;
        while (j < 10) {
            cout << std::setw(4) << A[i][j];  // Выводим элемент с шириной 4 символа
            j++;
        }
        cout << '\n';  // Переход на новую строку
        i++;
    }
    cout << '\n';  // Дополнительный перевод строки

    return 0;  // Завершение программы
}